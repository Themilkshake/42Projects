#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	int	c;
	int	k;

	k = -1;
	while (str[++k] != '\0')
	{
		c = str[k];
		if ((65 > c) || (c > 90))
			return (0);
	}
	return (1);
}
