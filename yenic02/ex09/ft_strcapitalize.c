#include <unistd.h>

char	*ft_strcapitalize(char *str)
{
	int	k;

	k = 0;
	while (str[k] != '\0')
	{
		if ((65 <= str[k]) && (str[k] <= 90))
			str[k] += 32;
		k++;
	}
	k = 0;
	if ((97 <= str[0]) && (str[0] <= 122))
		str[0] -= 32;
	k = 1;
	while (str[k] != '\0')
	{
		if (((97 <= str[k]) && (str[k] <= 122)) && !(((65 <= str[k - 1])
					&& (str[k - 1] <= 90))
				|| ((97 <= str[k - 1]) && (str[k - 1] <= 122))
				|| ((48 <= str[k - 1]) && (str[k - 1] <= 57))))
			str[k] -= 32;
		k++;
	}
	return (str);
}
