#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	a;
	int	k;

	k = 0;
	while (str[k] != '\0')
	{
		a = str[k];
		if ((97 <= a) && (a <= 122))
			str[k] -= 32;
		k++;
	}
	return (str);
}
