#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	k;
	int	a;

	k = 0;
	while (str[k] != '\0')
	{
		a = str[k];
		if ((65 <= a) && (a <= 90))
			str[k] += 32;
		k++;
	}
	return (str);
}
